<h1 class="mb-2 text-3xl font-semibold">Introduction</h1>
<p class="mb-3 text-justify">
  This study uses Artificial Intelligence methods to try and find strategies for
  the game of 2048 that a human player can understand. The purpose of this site
  is to gather data of people trying to follow a strategy, so the ability of
  people to actually understand a strategy can be evaluated.
</p>
<h2 class="mb-3 font-semibold">Please do the following:</h2>
<ol class="pl-4 mt-2 mb-6 text-justify list-decimal list-outside">
  <li class="mb-2">
    (Optional) If you have never played the game before or need a refresher
    please
    <a class="font-semibold underline" href="2048">go to the 2048 page</a> and
    play for a short amount of time so you get some understanding of how the
    game works. It will be hard to understand the strategy without a basic
    understanding of the game.
  </li>
  <li class="mb-2">
    Read the
    <a class="font-semibold underline" href="tutorial">tutorial.</a> This
    explains how the strategy works.
  </li>
  <li class="mb-2">
    Do the <a class="font-semibold underline" href="testing">testing.</a>
    Before starting the testing you should do some practice. Try to go through
    the strategy and check your answer. We recommend keeping the tutorial page
    open in a separate tab so you can quickly switch between them if needed.
    When you are confident you understand the strategy hide the answer and stop
    checking it. Make the moves you think the strategy would make. If you show
    the answer the data will not be saved and included in the study. Do this for
    as long as you are willing, don't feel the need to finish a full game if it
    is taking a long time.
  </li>
  <li class="mb-2">
    (Optional)
    <a class="font-semibold underline" href="ai">Check out an AI</a> playing the
    game that has not been designed to be understood by a person, it often
    reaches 32678!
  </li>
</ol>
